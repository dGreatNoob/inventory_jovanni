                        $branchTotal = 0;
                    @endphp
                    @foreach($products as $product)
                        @php
                            $item = $branchAllocation->items->where('product_id', $product->id)->first();
                            $quantity = $item ? $item->quantity : 0;
                            $branchTotal += $quantity;
                        @endphp
                        <td class="quantity-cell">{{ $quantity }}</td>
                    @endforeach
