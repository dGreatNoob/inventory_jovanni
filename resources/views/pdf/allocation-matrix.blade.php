                    @php
                        $productTotal = $branchAllocations->sum(function($branchAllocation) use ($product) {
                            $item = $branchAllocation->items->where('product_id', $product->id)->first();
                            return $item ? $item->quantity : 0;
                        });
                        $grandTotal += $productTotal;
                    @endphp
